class Solution
{
  public:
    vector<int> findIndex(int arr[], int n, int key)
    {
        //code here.
          int start = 0, end = n-1;
        while(start <= end){
            if(arr[start] != key){
                start++;
            }
            else if(arr[end] != key){
                end--;
            }
            else {
                return {start,end};
            }
        }
        return {-1,-1};
    }
  
};

